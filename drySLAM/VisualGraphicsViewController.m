//quaternion to x,y,z
-(float*)q2r:(float*)q{
    float x = q[2];
    float y = q[3];
    float z = q[4];
    float r = q[1];
    float* R = (float*)malloc(9*sizeof(float));
    R[0] = r*r+x*x-y*y-z*z;
    R[1] = 2*(x*y-r*z);
    R[2] = 2*(z*x+r*y);
    R[3] = 2*(x*y+r*z);
    R[4] = r*r-x*x+y*y-z*z;
    R[5] = 2*(y*z-r*x);
    R[6] = 2*(z*x-r*y);
    R[7] = 2*(y*z+r*x);
    R[8] = r*r-x*x-y*y+z*z;
    return R;
}

-(void)drawCameraAtLocation:(float*)location locationArraySize:(int)location_size{
    if (location_size!=7){
        NSLog(@"PLOT ERROR: numVertices must =3 (have %d)", location_size);
        return;
    }
    float q[4] = {location[4], location[5], location[6], location[7]};
    float* r = [self q2r:q];
    GLfloat* vertices = (GLfloat*)malloc(9*sizeof(GLfloat));
    vertices[0] = cameraVertexData[0]*r[0] + location[0]; //1x
    vertices[1] = cameraVertexData[1]*r[1] + location[0]; //1y
    vertices[2] = cameraVertexData[2]*r[2] + location[0]; //1z
    vertices[3] = cameraVertexData[3]*r[3] + location[1];
    vertices[4] = cameraVertexData[4]*r[4] + location[1];
    vertices[5] = cameraVertexData[5]*r[5] + location[1];
    vertices[6] = cameraVertexData[6]*r[6] + location[2];
    vertices[7] = cameraVertexData[7]*r[7] + location[2];
    vertices[8] = cameraVertexData[8]*r[8] + location[2];
    
    
    free(vertices);
    free(r);
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    
    self.context = [[EAGLContext alloc] initWithAPI:kEAGLRenderingAPIOpenGLES2];
    
    if (!self.context) {
        NSLog(@"Failed to create ES context");
    }
    
    GLKView *view = (GLKView *)self.view;
    view.context = self.context;
    view.drawableDepthFormat = GLKViewDrawableDepthFormat24;
    
    for(int i=0; i<12; ++i){
        cameraVertexData[i] = cameraVertexData[i]*CAMERA_SIZE;
    }
    
    //DEBUG:
    
    [self setupGL];
}

- (void)dealloc
{
    [self tearDownGL];
    
    if ([EAGLContext currentContext] == self.context) {
        [EAGLContext setCurrentContext:nil];
    }
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    
    if ([self isViewLoaded] && ([[self view] window] == nil)) {
        self.view = nil;
        
        [self tearDownGL];
        
        if ([EAGLContext currentContext] == self.context) {
            [EAGLContext setCurrentContext:nil];
        }
        self.context = nil;
    }
    
    // Dispose of any resources that can be recreated.
}

